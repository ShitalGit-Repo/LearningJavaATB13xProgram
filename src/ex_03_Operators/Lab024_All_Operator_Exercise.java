package ex_03_Operators;

public class Lab024_All_Operator_Exercise {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        boolean b1 = true;

        System.out.println(a + b);             //Arithmetic Operator.
        System.out.println(a > b);             //Relational Operator.
        System.out.println(true || false);     //Logical - OR Operator.
        System.out.println(true && false);     //Logical - AND Operator.
        System.out.println(a%b);               //Modulus Operator.
        System.out.println(a == b);            //Equal Operator.

        //Other Operator that are not useful for Automation.
        // Useful for digital electronics only.

        // ~ -> Bitwise Operator
        // >> -> Right Shift
        // << -> Left Shift
        // ^ ->XOR
    }
}
