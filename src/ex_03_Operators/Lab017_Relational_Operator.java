package ex_03_Operators;

public class Lab017_Relational_Operator {
    public static void main(String[] args) {
        //Relational Operator -> >, >= ,< ,<= ,== ,=! -- will results in boolean output.

        int a = 10;
        int b = 30;
        boolean c = a>b;
        System.out.println(c);

        int age_mamitha = 33;
        int age_pramod = 34;
        boolean result = age_pramod >= age_mamitha;
        System.out.println(result);



    }
}
