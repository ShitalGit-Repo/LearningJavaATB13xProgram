package ex_03_Operators;

public class Lab021_Equal_Operator {
    public static void main(String[] args) {
        //Equal Operator (==)-- compares the value in case of primitive data.

        System.out.println(10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 >= 10);

        //Interview Question
        System.out.println(!(10>20));

    }
}
