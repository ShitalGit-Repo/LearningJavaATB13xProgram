package ex_10_Loops;

public class Lab043_For_If_Loop {
    public static void main(String[] args) {
        for (int shital=0; shital<20; shital++){
            if (shital>18){
                System.out.println("Eligible for voting");
            }else{
                System.out.println("Not eligible to vote");
            }
        }
    }
}
