package ex_02_Literals;

public class Lab009_Types_of_Literal {
    public static void main(String[] args) {
        int age = 65;    //here 65 is literal it is a integer literal
        float pi = 3.14f;
        boolean is_he_married = true;  //you cannot assign numeric or any other value.
        int Enum = 56;   //it is valid bcz "enum" is the keyword not Enum.

        System.out.println(age);
        System.out.println(pi);
        System.out.println(is_he_married);
        System.out.println(Enum);

    }
}
